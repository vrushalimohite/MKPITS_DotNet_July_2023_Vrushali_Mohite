//  create a array using new keyword
using System;
namespace program
{
    class program
    {
        static void Main(string[]args)
        {
            int[] num = new int[3];
            num[0] = 55;
            num[1] = 70;
            num[2] = 80;
            Console.WriteLine("num[0] :" + num[0]);
            Console.WriteLine("num[1] :" + num[1]);
            Console.WriteLine("num[2] :" + num[2]);
            Console.ReadKey();
        }
    }
}
